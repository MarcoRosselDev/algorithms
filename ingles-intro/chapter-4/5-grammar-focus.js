/* Possessives |

Adjetives--------------------------------
    el objeto se nombra por primera vez
            my
            your
These are   his    shoes.
            her
            our
            their

Pronouns----------------------------------
    ya se nombro el objeto
                mine
                yours
These shoes are his
                hers
                ours
                theirs

Names--------------------------------------

Jack's tie
Taylor's shoes.
Alex's coat.
Whose tie is this ?         It's Greg's.
Whose shoes are these ?     They're Taylor's

Diferencias de adjetivo y pronombres---->
    ¿Y la tuya? La diferencia entre adjetivos y pronombres estriba en que los primeros acompañan a un sustantivo (aparece antes del nombre común o nombre propio), mientras que los segundos van en su lugar (lo remplazan), de ahí que exista forma neutra en los pronombres y no en los adjetivos.

*/

