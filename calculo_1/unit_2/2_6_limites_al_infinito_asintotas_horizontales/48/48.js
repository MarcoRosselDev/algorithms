function limite_48(n) {
  const numerator = (2 * n ** 2 + 1) ** 0.5;
  const denominator = 3 * n - 5;
  return numerator / denominator;
}

const calcular = [
  1, 1.5, 1.7, 1.8, 2, 1000, 10000, 100000, -1000, -10000, -100000,
];

for (let i = 0; i < calcular.length; i++) {
  console.log(`${calcular[i]} : ${limite_48(calcular[i])}`);
}

